    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        if (txtNombre.getText().length() == 0){
        JOptionPane.showConfirmDialog(rootPane, "Debes insertar Nombre");
        txtNombre.requestFocus();
        return;
        }
        if (txtPais.getText().length() == 0){
        JOptionPane.showConfirmDialog(rootPane, "Debes insertar un Pais");
        txtPais.requestFocus();
        return;
        }
        if (txtEdadAnimal.getText().length() == 0){
        JOptionPane.showConfirmDialog(rootPane, "Debes insertar una edad");
        txtEdadAnimal.requestFocus();
        return;
        }
        if (txtPesoAnimal.getText().length() == 0){
        JOptionPane.showConfirmDialog(rootPane, "Debes insertar un Peso");
        txtPesoAnimal.requestFocus();
        return;
        }
        Animal ani = new Animal();
        Zoologico func = new Zoologico();
        String nombre,pais;
        Double peso; int edad;
        
        nombre = txtNombre.getText();
        pais = txtPais.getText();
        peso = Double.parseDouble(txtPesoAnimal.getText());
        edad = Integer.parseInt(txtEdadAnimal.getText());
       func.insertar(0,nombre,pais,peso,edad);
    }//GEN-LAST:event_btnInsertarActionPerformed

